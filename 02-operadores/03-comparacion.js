let a = 10
console.log(a > 5)// true
console.log(a < 5)// false
console.log(a >= 10)// true
console.log(a <= 10)// true

console.log(a == 10)// true
console.log(a != 10)// false

// important == only compare values, not types
console.log(a == '10')// true XD

// important === compare values and types
console.log(a === '10')// false
console.log(a !== '10')// true


